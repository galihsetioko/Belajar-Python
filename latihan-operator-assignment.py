''' 
operator assignment berfungsi untuk memasukkan nilai ke dalam sebuah variabel dan 
memerintahkan komputer untuk mengalokasikan sejumlah memori untuk menyimpan nilai yang diberikan ( kurang lebih definisinya seperti itu )
operator assignment diantaranya sebagai berikut : 
+= , -= , *= , /= , **= , //=

'''

a = 3
print('Nilai a = 3')

a += 3
print("a += 3")
print("Nilai a menjadi",a)

a -= 3
print("a -= 3")
print("Nilai a menjadi",a)

a *= 3
print("a *= 3")
print("Nilai a menjadi",a)

a /= 3
print("a /= 3")
print("Nilai a menjadi",a)

a //= 3
print("a //= 3")
print("Nilai a menjadi",a)

a **= 3
print("a **= 3")
print("Nilai a menjadi",a)